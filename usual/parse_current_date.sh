#!/bin/bash

year=`date +%Y`
month=`date +%m`
day=`date +%d`

hour=`date +%H`
minute=`date +%M`
second=`date +%S`


echo `date`


echo "year is $year"
echo "month is $month"
echo "day is $day"
echo "hour is $hour"
echo "minute is $minute"
echo "second is $second"

echo "Current Date is: $day-$month-$year"
echo "Current Time is: $hour-$minute-$second"

